package oca_certification;

public class Q_2 {

	public static void main(String[] args) {
		// Quotient of two numbers and remainders of two numbers
		
		// Quotient = /
		// Remainder = %
				
		double num1 = 100, num2 = 3;
				
		// Quotient 
		System.out.println(num1/num2);
				
		// Remainder
		System.out.println(num1%num2);
	}

}
