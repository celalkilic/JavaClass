package java_coding_interview;

public class Question3 {

	public static void main(String[] args) {
		// check if the alphabet is vowel or consonant
		char alp = 'a';
		if (alp == 'a'|| alp=='e'|| alp=='u'|| alp=='i'|| alp=='o') {
			System.out.println(alp + " is a vowel");
		}else {
			System.out.println(alp + " is consonant");
		}

	}

}
