package week6monday;

public class MethodConcept3 {
	
	public static int calculate(int a, int b, int c) {
		return a + b + c;
	}

	public static void main(String[] args) {
		
		int number1 = 30;
		int number2 = 35;
		int number3 = 20;

		System.out.println("Sum of numbers : " + calculate(number1, number2, number3));
	

	}

}
